

console.log(` Logical AND &&`);
var num1 = 10;
var num2 = 20;
num2 && num1;  //num2 = num2 && num1;
console.log(`Logical AND is ${num2}`);

console.log(` Logical OR ||`);
var num1 = 10;
var num2 = 20;
num2 || num1;  //num2 = num2 || num1;
console.log(`Logical OR is ${num2}`);

console.log(` Logical NOT !`);
var num1 = 10;
var num2 = 20;
num2 = num1;  //num2 = num2 ! num1;
console.log(`Logical NOT is ${num2}`);